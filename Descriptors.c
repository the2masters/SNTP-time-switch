uint16_t CALLBACK_USB_GetDescriptor(const uint16_t wValue, __attribute__((unused)) const uint16_t wIndex, const void** const DescriptorAddress
//, uint8_t* const DescriptorMemorySpace
)
{
	const uint8_t  DescriptorType   = (wValue >> 8);
	const uint8_t  DescriptorNumber = (wValue & 0xFF);

	const void* Address = NULL;
	uint16_t    Size    = NO_DESCRIPTOR;
	//*DescriptorMemorySpace = MEMSPACE_FLASH;

	switch (DescriptorType)
	{
		case DTYPE_Device:
			Address = &DeviceDescriptor;
			Size    = sizeof(USB_Descriptor_Device_t);
			break;
		case DTYPE_Configuration:
			switch(DescriptorNumber)
			{
				case 0:	Address = &ConfigurationDescriptor;
					Size    = sizeof(USB_Descriptor_Configuration_t);
					break;
				case 1:	Address = &ConfigurationDescriptor2;
					Size    = sizeof(USB_Descriptor_Configuration2_t);
					break;
			}

			break;
		case DTYPE_String:
			switch (DescriptorNumber)
			{
				case STRING_ID_Language:
					Address = &LanguageString;
					Size    = pgm_read_byte(&LanguageString.Header.Size);
					break;
				case STRING_ID_Manufacturer:
					Address = &ManufacturerString;
					Size    = pgm_read_byte(&ManufacturerString.Header.Size);
					break;
				case STRING_ID_Product:
					Address = &ProductString;
					Size    = pgm_read_byte(&ProductString.Header.Size);
					break;
				case STRING_ID_MAC:
					Address = &MACString;
					Size    = pgm_read_byte(&MACString.Header.Size);
					//*DescriptorMemorySpace = MEMSPACE_EEPROM;
					break;
			}

			break;
	}

	*DescriptorAddress = Address;
	return Size;
}

